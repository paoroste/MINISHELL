#include "libft.h"

char	*ft_strcpy(char *dest, const char *src)
{
	int		i;
	char	*str;

	i = 0;
	str = (char*)src;
	while (str[i] != '\0')
	{
		dest[i] = str[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
