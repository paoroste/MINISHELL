#include "libft.h"

void	ft_putendl(char const *s)
{
	char	*str;
	int		i;

	str = (char*)s;
	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
	ft_putchar('\n');
}
